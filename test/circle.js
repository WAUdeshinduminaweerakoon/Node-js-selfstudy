/* 
Find the diameter, area and circumference of radius 20cm.
Diameter pf a Circale D= 21*r
Circumference of a Circle C=2*PI*r
Area of a Circle  A= PI*r*r
PI = 3.141
*/
let radius = 20;
const PI = Math.PI;
let diameter;
diameter =2*radius;
let circumference = 2*PI*radius;
let area =PI*radius**2;

console.log("Diameter :- "+diameter.toFixed(3));
console.log("Circumference :- " +circumference.toFixed(3));
